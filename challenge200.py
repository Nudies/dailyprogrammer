def makeWorld(map):
    map = map.strip().split('\n')
    world = [[] for i in range(len(map))]
    for i in range(len(world)):
        for c in map[i]:
            world[i].append(c)
    return world


def floodFill(world, x, y, oldC, newC):
    if world[y][x] != oldC:
        return

    world[y][x] = newC
    if x < len(world[0]) - 1:
        floodFill(world, x+1, y, oldC, newC)
    if x > 0:
        floodFill(world, x-1, y, oldC, newC)
    if y < len(world) - 1:
        floodFill(world, x, y+1, oldC, newC)
    if y > 0:
        floodFill(world, x, y-1, oldC, newC)


